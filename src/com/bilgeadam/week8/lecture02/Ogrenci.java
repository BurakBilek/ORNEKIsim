package com.bilgeadam.week8.lecture02;

public class Ogrenci {
	int id;
	String name;

	public Ogrenci(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
